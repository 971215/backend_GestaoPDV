    private void preencherSessao(Usuario u) {
        Sessao.id = u.getId();
        Sessao.login = u.getLogin();
        Sessao.nome = u.getNome();
        Sessao.perfil = u.getPerfil();
        Sessao.senha = u.getSenha();
    }

    private void logar() {
        UsuarioService service = new UsuarioService(new UsuarioDAO(), new AutenticacaoDAO());
        try {
            Usuario u = service.getUsuario(jtfUsuario.getText(), jtfSenha.getText());
            preencherSessao(u);
            dispose();
            PrincipalView view =  new PrincipalView();
            view.setVisible(true);

        } catch (RuntimeException e) {
            JOptionPane.showMessageDialog(null, "Usuario e/ou senha inv√°lido(s)!");
        }
    }

